public class Pixel {

    private int x;
    private int y;

    /**
     * Constructor.
     * @param x the x
     * @param y the y
     */
    public Pixel(int x, int y) {
        this.x = x;
        this.y = y;
    }
    /**
     * Gets x coordinate.
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * Gets y coordinate.
     * @return  y
     */
    public int getY() {
        return y;
    }
}

